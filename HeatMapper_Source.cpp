void heatMapper(int board[][100], int x, int y)
{
	oneUnitMapper(board, x, y);
	twoUnitMapper(board, x, y);
	threeUnitMapper(board, x, y);
}

void oneUnitMapper(int board[][100], int x, int y)
{

	for (int i = 0; i < x; i++)
	{
		for (int j = 0; j < y; j++)
		{
			if (board[i][j] == 0)
			{
				for (int k = -1; k < 2; k++)
				{
					for (int p = -1; p < 2; p++)
					{
						if (i + k >= 0 && j + p >= 0 && i + k < x && j + p < y)
						{
							if (board[i + k][j + p] == 4)
							{
								board[i + k][j + p] = 1;
							}
						}
					}
				}
			}
		}
	}
}

void twoUnitMapper(int board[][100], int x, int y)
{
	for (int i = 0; i < x; i++)
	{
		for (int j = 0; j < y; j++)
		{
			if (board[i][j] == 1)
			{
				for (int k = -1; k < 2; k++)
				{
					for (int p = -1; p < 2; p++)
					{
						if (i + k >= 0 && j + p >= 0 && i + k < x && j + p < y)
						{
							if (
								board[i + k][j + p] != 0 &&
								board[i + k][j + p] != 1 &&
								board[i + k][j + p] != 2 &&
								board[i + k][j + p] != 5 &&
								board[i + k][j + p] != 9
								)
							{
								board[i + k][j + p] = 2;
							}
						}
					}
				}
			}
		}
	}
}

